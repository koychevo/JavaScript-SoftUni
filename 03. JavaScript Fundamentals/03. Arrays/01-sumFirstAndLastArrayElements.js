function solve(numbers) {
     let firstNum = Number(numbers[0]);
     let secondNum = Number(numbers[numbers.length - 1]);
     console.log(firstNum + secondNum);
}

solve(['20', '30', '40']);
solve(['10', '17', '22', '33']);
solve(['11', '58', '69']);

